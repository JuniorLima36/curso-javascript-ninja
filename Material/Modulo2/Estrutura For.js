/*
# for
A instrução for cria um loop que consiste em três expressões opcionais, dentro de parênteses 
e separadas por ponto e vírgula, seguidas por uma declaração ou uma sequência de declarações
executadas em sequência.

Sintaxe
for ([inicialização]; [condição]; [expressão final])
   declaração


Exemplo:
A declaração for começa declarando a variável i e inicializando-a como 0. Ela verifica se i
é menor que nove, executa as duas instruções subsequentes e incrementa 1 a variável i após 
cada passagem pelo loop.

for (var i = 0; i < 9; i++) {
   console.log(i);
   // more statements
}
*/